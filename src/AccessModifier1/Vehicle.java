package AccessModifier1;

public class Vehicle {
    void brand() {
        System.out.println("The manufacturer is: Toyota");
    }

    public void model() {
        System.out.println("The Model is: Innova");
    }

    protected void price() {
        System.out.println("Price is: 20 Lakhs");
    }

    private void vehicleHistory() {
        System.out.println("Private History");
    }
}


